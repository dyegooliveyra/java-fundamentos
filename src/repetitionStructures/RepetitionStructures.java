package repetitionStructures;

public class RepetitionStructures {

    public static void main(String[] args) {
    //while
        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }

    //do-while
        int j = 0;
        do {
            System.out.println(j);
            j++;
        } while (j < 5);

    //for
        for (int k = 0; k < 5; k++) {
            System.out.println(k);
        }
    }
}
